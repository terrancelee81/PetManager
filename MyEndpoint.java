package com.example.tlee1.myapplication.backend;

import com.google.api.server.spi.config.Api;
import com.google.api.server.spi.config.ApiMethod;
import com.google.api.server.spi.config.ApiNamespace;
import com.google.appengine.api.datastore.DatastoreService;
import com.google.appengine.api.datastore.DatastoreServiceFactory;
import com.google.appengine.api.datastore.Entity;

import javax.inject.Named;

/** An endpoint class we are exposing */
@Api(
  name = "myApi",
  version = "v1",
  namespace = @ApiNamespace(
    ownerDomain = "backend.myapplication.tlee1.example.com",
    ownerName = "backend.myapplication.tlee1.example.com",
    packagePath=""
  )
)
public class MyEndpoint {

    /** A simple endpoint method that takes a name and says Hi back */
    @ApiMethod(name = "sayHi")
    public MyBean sayHi(@Named("name") String name) {
        MyBean response = new MyBean();

        DatastoreService ds = DatastoreServiceFactory.getDatastoreService();

        //create entity to add to the datastore

        Entity e = new Entity("Data");
        e.setProperty("User Entered Value", name);

        response.setData(name);

        //send data to datastore
        ds.put(e);

        return response;
    }

}